#Series 0 to n
a = int(input("Enter a number"))
for i in range(0,(a+1),1):
    print(i)
#Series n to 0
b = int(input("Enter a number"))
for i in range(b,-1,-1):
    print(i)
#Series 2 to n
c = int(input("Enter a number: \n"))
for i in range(2,(c+1),2):
    print(i)
#Series 1 to n
d = int(input("Enter a number: \n"))
for i in range(1,(d+1),2):
    print(i)